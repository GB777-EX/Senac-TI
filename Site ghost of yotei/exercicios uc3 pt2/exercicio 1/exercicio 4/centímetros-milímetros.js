var metros
var centimetros = 100
var milimetros = 1000

var metros = prompt ("digite um valor em metros")

var metros = Number (metros)

centimetros = metros * centimetros
milimetros = metros * milimetros

alert (`seu valor em metros é ${metros}m`)
alert (`seu valor em centimetros é ${centimetros}cm`)
alert (`seu valor em milimetros é ${milimetros}mm`)